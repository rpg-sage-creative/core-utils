export var TemperateSeason;
(function (TemperateSeason) {
    TemperateSeason[TemperateSeason["Winter"] = 1] = "Winter";
    TemperateSeason[TemperateSeason["Spring"] = 2] = "Spring";
    TemperateSeason[TemperateSeason["Summer"] = 3] = "Summer";
    TemperateSeason[TemperateSeason["Fall"] = 4] = "Fall";
})(TemperateSeason || (TemperateSeason = {}));
