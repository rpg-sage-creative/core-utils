export var Season;
(function (Season) {
    Season[Season["Winter"] = 1] = "Winter";
    Season[Season["Spring"] = 2] = "Spring";
    Season[Season["Summer"] = 3] = "Summer";
    Season[Season["Fall"] = 4] = "Fall";
    Season[Season["Wet"] = 5] = "Wet";
    Season[Season["Dry"] = 6] = "Dry";
})(Season || (Season = {}));
